<h2>Introduzione</h2>

<p>
Questa funzione permette di amministrare il repository di un archivio dCTL sul server, centralizzando tutte le operazioni di gestione delle risorse.<br />
<br />
Quasi tutte le operazioni di gestione avvengono tramite l'interfaccia utente offerta dal sistema.<br />
Solo quando non è possibile effettuare le operazioni sul server (p.es. la codifica XML) è previsto il download del materiale come archivio compresso in formato ZIP.<br />
<br />
&Egrave; possibile creare, inviare, scaricare e pubblicare:<br />
&#160;&#160;- le collection;<br />
&#160;&#160;- i package.<br />
&Egrave; possibile caricare e ottenere gli identificativi per:<br />
&#160;&#160;- i file multimediali.<br />
&Egrave; possibile inoltre:<br />
&#160;&#160;- creare le mappature tra testo e immagini.<br />
&#160;&#160;- gestire i collegamenti tra le risorse.<br />
<br />
